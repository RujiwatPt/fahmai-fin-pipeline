def _load_questions() -> dict:
    qmap = {}
    with QUESTIONS_CSV.open(encoding="utf-8") as f:
        for row in csv.DictReader(f):
            qmap[row["id"]] = row["question"]
    return qmap


def main():
    qmap = _load_questions()
    missing = [qid for qid, _, _ in ROWS if qid not in qmap]
    if missing:
        print("WARNING: ids not found in questions.csv:", missing)
    with OUT.open("w", encoding="utf-8-sig", newline="") as f:
        w = csv.writer(f)
        w.writerow(["id", "question", "answer", "explain"])
        for qid, answer, explain in ROWS:
            w.writerow([qid, qmap.get(qid, ""), answer, explain])
    print(f"wrote {len(ROWS)} rows -> {OUT}")


if __name__ == "__main__":
    main()
